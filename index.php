var coords = {};
function getdata(){
  $.post("wsdata.php",function(data,status){
    if(status==="success"){
      var obj = jQuery.parseJSON(data);
      //console.log(obj); 
      $.each(obj,function(index,element){
        if(element.ws_content!=="0" && element.ws_content!==null){
          var con = "ws".concat(element.ws_name);
          $(".".concat(con.concat("content"))).text(element.ws_content);
          if(element.ws_venue !== null) {
            $(".".concat(con.concat("venue"))).text(element.ws_venue);
          }
          if(element.ws_time !== null){
            $(".".concat(con.concat("time"))).text(element.ws_time);
          }
          if(element.ws_coords !== null){
             coords[con] = JSON.parse(element.ws_coords);
           }
        }
      });
    }
  });
  $.post("evdata.php",function(data,status){
    if(status==="success"){
      //console.log(data);
      var obj = jQuery.parseJSON(data);
      //console.log(obj); 
      $.each(obj,function(index,element){
        if(element.ev_content!=="0" && element.ev_content!==null){
          var con = "ev".concat(element.ev_name);
          console.log(con);
          $(".".concat(con.concat("content"))).text(element.ev_content);
