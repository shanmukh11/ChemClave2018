            $(".".concat(con.concat("venue"))).text(element.ev_venue);
          }
          if(element.ev_time !== null){
            $(".".concat(con.concat("time"))).text(element.ev_time);
          }
          if(element.ev_prize !== null){
            $(".".concat(con.concat("prize"))).text(element.ev_prize);
